use actix_service::{Service, Transform};
use actix_web::{
    Error,
    dev::ServiceRequest,
    dev::ServiceResponse,
    http::header::{HeaderName, HeaderValue},
};
use futures_util::future::{self, LocalBoxFuture, Ready};
use std::task::{Context, Poll};
use std::time::Instant;
use tracing::{Instrument, Level, info, span};
use uuid::Uuid;
use crate::metrics;

const CORRELATION_ID: HeaderName = HeaderName::from_static("x-correlation-id");
const REQUEST_ID: HeaderName = HeaderName::from_static("x-request-id");

// Middleware factory
pub struct TracingMiddleware;

impl<S, B> Transform<S, ServiceRequest> for TracingMiddleware
where
    S: Service<ServiceRequest, Response = ServiceResponse<B>, Error = Error> + 'static,
    S::Future: 'static,
    B: 'static,
{
    type Response = ServiceResponse<B>;
    type Error = Error;
    type Transform = TracingMiddlewareService<S>;
    type InitError = ();
    type Future = Ready<Result<Self::Transform, Self::InitError>>;

    fn new_transform(&self, service: S) -> Self::Future {
        future::ready(Ok(TracingMiddlewareService { service }))
    }
}

pub struct TracingMiddlewareService<S> {
    service: S,
}

impl<S, B> Service<ServiceRequest> for TracingMiddlewareService<S>
where
    S: Service<ServiceRequest, Response = ServiceResponse<B>, Error = Error> + 'static,
    B: 'static,
{
    type Response = ServiceResponse<B>;
    type Error = Error;
    type Future = LocalBoxFuture<'static, Result<Self::Response, Self::Error>>;

    fn poll_ready(&self, cx: &mut Context<'_>) -> Poll<Result<(), Self::Error>> {
        self.service.poll_ready(cx)
    }

    fn call(&self, req: ServiceRequest) -> Self::Future {
        let correlation_id = req
            .headers()
            .get(&CORRELATION_ID)
            .and_then(|hv| hv.to_str().ok())
            .map(str::to_string);

        let request_id = Uuid::new_v4().to_string();
        let span = span!(
            Level::INFO,
            "request",
            request_id     = %request_id,
            correlation_id = ?correlation_id
        );

        let method = req.method().to_string();
        let path = req.path().to_string();
        let client_ip = req
            .connection_info()
            .realip_remote_addr()
            .map(|s| s.split(':').next().unwrap_or(s).to_string());

        let start_time = Instant::now();
        info!(request_id = %request_id, correlation_id = ?correlation_id, message = "Request start", method = &method, path = &path);

        let fut = self.service.call(req);

        Box::pin(
            async move {
                let mut res = fut.await?;
                
                let elapsed_time = start_time.elapsed();
                info!(message = "Request end", request_id = %request_id, correlation_id = ?correlation_id, method = &method, path = &path, run_time = ?elapsed_time, status_code = ?res.status());

                // Record HTTP metrics
                let status_code = res.status().as_u16();
                metrics::http_metrics().observe(
                    &method,
                    &path,
                    status_code,
                    client_ip.as_deref(),
                    elapsed_time.as_secs_f64(),
                );

                res.headers_mut().insert(
                    REQUEST_ID,
                    request_id.parse().unwrap_or_else(|_| HeaderValue::from_static("<failed>")),
                );

                if let Some(correlation_id) = correlation_id {
                    res.headers_mut().insert(
                        CORRELATION_ID,
                        correlation_id.parse().unwrap_or_else(|_| HeaderValue::from_static("<failed>"))
                    );
                }
                Ok(res)
            }
            .instrument(span),
        )
    }
}

